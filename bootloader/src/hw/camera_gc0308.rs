// hw/camera.rs — GC0308 camera sensor driver for M5Stack CoreS3
// 100% Rust, no-std, no-alloc
//
// Hardware: GC0308 0.3MP camera (640x480 max)
//   - DVP 8-bit parallel interface via LCD_CAM peripheral
//   - SCCB (I2C) control at address 0x21
//   - XCLK generated by ESP32-S3 LEDC on GPIO2
//   - Camera reset via AW9523B IO expander P1_0
//
// CoreS3 Pin Map:
//   PCLK  = GPIO45     VSYNC = GPIO46     HREF = GPIO38
//   D0    = GPIO39      D1   = GPIO40      D2  = GPIO41
//   D3    = GPIO42      D4   = GPIO15      D5  = GPIO16
//   D6    = GPIO48      D7   = GPIO47
//   SCCB SDA = GPIO12 (shared I2C bus)
//   SCCB SCL = GPIO11 (shared I2C bus)
//
// Init: Espressif gc0308_sensor_default_regs + subsample QVGA via Page 1 regs
// Capture: Stock esp-hal Camera::receive()/wait() API, DMA to SRAM

