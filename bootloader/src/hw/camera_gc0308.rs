/// Enable LCD_CAM system clock, configure XCLK output on GPIO2 (~10MHz).
///
/// Must be called BEFORE `LcdCam::new()` so that register writes stick.
/// Sets up:
///   - SYSTEM.PERIP_CLK_EN1 — LCD_CAM peripheral clock
///   - LCD_CLOCK_REG — lcd_clk_en, clk_sel=PLL160M, div=2
///   - CAM_CTRL_REG — cam_clk_sel=PLL160M, div=16 → 10MHz XCLK
///   - GPIO matrix — route CAM_CLK (signal 149) → GPIO2
pub fn enable_lcd_cam_clocks() {
    unsafe {
        // Enable LCD_CAM peripheral clock + reset cycle
        let clk_en1_addr = 0x600C_001Cu32 as *mut u32;
        let rst_en1_addr = 0x600C_0024u32 as *mut u32;
        let clk_en1 = core::ptr::read_volatile(clk_en1_addr);
        if clk_en1 & 1 == 0 {
            core::ptr::write_volatile(clk_en1_addr, clk_en1 | 1);
            let rst = core::ptr::read_volatile(rst_en1_addr);
            core::ptr::write_volatile(rst_en1_addr, rst | 1);
            for _ in 0..100u32 { core::ptr::read_volatile(&0u32); }
            core::ptr::write_volatile(rst_en1_addr, rst & !1u32);
            for _ in 0..100u32 { core::ptr::read_volatile(&0u32); }
        }

        // Enable lcd_clk_en (bit 31) + clk_sel=PLL160M (bits 30:29=2) + div=2 (bits 15:8)
        let lcd_clock_addr = 0x6004_1000u32 as *mut u32;
        let lcd_clk = core::ptr::read_volatile(lcd_clock_addr);
        if lcd_clk & (1u32 << 31) == 0 {
            let lcd_clock_val = (1u32 << 31) | (2u32 << 29) | (2u32 << 8);
            core::ptr::write_volatile(lcd_clock_addr, lcd_clock_val);
        }

        // Configure CAM_CTRL: clk_sel=PLL160M, div_num=16 → 10MHz XCLK
        let cam_ctrl_addr = 0x6004_1004u32 as *mut u32;
        let cam_ctrl_pre = core::ptr::read_volatile(cam_ctrl_addr);
        let mut cam_ctrl_val = cam_ctrl_pre;
        cam_ctrl_val &= !(0xFF | (0xFF << 8) | (3 << 16) | (3 << 18));
        cam_ctrl_val |= 16;        // div_num = 16
        cam_ctrl_val |= 2 << 18;   // clk_sel = 2 (PLL160M)
        core::ptr::write_volatile(cam_ctrl_addr, cam_ctrl_val);

        // Route CAM_CLK (signal 149) → GPIO2
        let func_out_cfg_base = 0x6000_4554u32;
        let gpio2_out_cfg = (func_out_cfg_base + 2 * 4) as *mut u32;
        core::ptr::write_volatile(gpio2_out_cfg, 149u32);
        // Enable GPIO2 output
        let gpio_en0 = 0x6000_4020u32 as *mut u32;
        let en = core::ptr::read_volatile(gpio_en0);
        core::ptr::write_volatile(gpio_en0, en | (1u32 << 2));
        // IO_MUX GPIO2 → GPIO function (MCU_SEL=1)
        let iomux_gpio2 = (0x6000_9000u32 + 0x04 + 2 * 4) as *mut u32;
        let mux_val = core::ptr::read_volatile(iomux_gpio2);
        core::ptr::write_volatile(iomux_gpio2, (mux_val & !0x7000) | 0x1000);
    }
}

/// Ensure LCD_CLOCK.CLK_EN is set. Call right before `Camera::new()`.
pub fn ensure_lcd_clk_enabled() {
    unsafe {
        let lcd_clk = core::ptr::read_volatile(0x6004_1000u32 as *const u32);
        if lcd_clk & (1u32 << 31) == 0 {
            core::ptr::write_volatile(0x6004_1000u32 as *mut u32, lcd_clk | (1u32 << 31));
        }
    }
}

/// Verify XCLK is toggling on GPIO2. Returns number of edges detected in ~10k samples.
/// A value >100 means XCLK is running.
pub fn verify_xclk_running() -> u32 {
    unsafe {
        let in0 = 0x6000_403Cu32 as *const u32;
        let mut tog = 0u32;
        let mut last = (core::ptr::read_volatile(in0) >> 2) & 1;
        for _ in 0..10_000u32 {
            let c = (core::ptr::read_volatile(in0) >> 2) & 1;
            if c != last { tog += 1; last = c; }
        }
        tog
    }
}

/// Configure GPIO matrix routing for all DVP camera signals.
///
/// Maps physical GPIO pins to LCD_CAM input signals via func_in_sel_cfg.
/// Also enables input buffers on all camera pins via IO_MUX.
///
/// Pin map (M5Stack CoreS3):
///   PCLK=GPIO45, VSYNC=GPIO46, HREF=GPIO38
///   D0=39, D1=40, D2=41, D3=42, D4=15, D5=16, D6=48, D7=47
pub fn setup_cam_gpio_routing() {
    unsafe {
        let gpio = &*esp_hal::peripherals::GPIO::PTR;
        let io_mux = &*esp_hal::peripherals::IO_MUX::PTR;

        // Enable input buffer + GPIO function on all camera pins
        let cam_gpios: [u8; 11] = [45, 46, 38, 39, 40, 41, 42, 15, 16, 47, 48];
        for &pin in &cam_gpios {
            io_mux.gpio(pin as usize).modify(|_, w| {
                w.fun_ie().set_bit();
                w.mcu_sel().bits(1)
            });
        }

        // Route GPIO → LCD_CAM input signal via GPIO matrix
        let route = |signal_idx: usize, gpio_num: u8| {
            gpio.func_in_sel_cfg(signal_idx).write(|w| {
                w.bits(0x80 | gpio_num as u32)
            });
        };

        route(149, 45);  // CAM_PCLK_IDX ← GPIO45
        route(152, 46);  // CAM_V_SYNC_IDX ← GPIO46
        gpio.func_in_sel_cfg(151).write(|w| w.bits(0x80 | 0x3C)); // CAM_H_SYNC ← const 1
        route(150, 38);  // CAM_H_ENABLE_IDX ← GPIO38 (HREF)
        // CAM_DATA_IN0..7 = signals 133..140
        route(133, 39);  route(134, 40);  route(135, 41);  route(136, 42);
        route(137, 15);  route(138, 16);  route(139, 48);  route(140, 47);
    }
}

/// Configure LCD_CAM for VSYNC-based EOF and HREF data gating.
///
/// Sets cam_vs_eof_en=1 and cam_vh_de_mode_en=1 in CAM_CTRL / CAM_CTRL1.
pub fn configure_cam_vsync_eof() {
    unsafe {
        let cam_ctrl_addr = 0x6004_1004u32 as *mut u32;
        let cam_ctrl1_addr = 0x6004_1008u32 as *mut u32;
        let cur = core::ptr::read_volatile(cam_ctrl_addr);
        core::ptr::write_volatile(cam_ctrl_addr, cur | (1u32 << 22) | (1u32 << 31));
        let cur1 = core::ptr::read_volatile(cam_ctrl1_addr);
        core::ptr::write_volatile(cam_ctrl1_addr, cur1 | 1u32);
    }
}
