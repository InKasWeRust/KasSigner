/// Detect the GC0308 camera on I2C bus.
/// Returns true if chip ID = 0x9B.
pub fn detect_gc0308<I2C: embedded_hal::i2c::I2c>(i2c: &mut I2C) -> bool {
    // Select page 0
    sccb_write(i2c, REG_PAGE_SELECT, 0x00);
    if let Some(id) = sccb_read(i2c, REG_CHIP_ID) {
        id == GC0308_CHIP_ID
    } else {
        false
    }
}

/// Initialize the GC0308 camera for QVGA (320x240) grayscale output.
///
/// Steps:
///   1. Hardware reset via AW9523B
///   2. Write default register table
///   3. Configure subsample mode for QVGA
///   4. Set output format to Y-only (grayscale 8-bit)
///
/// Returns Ok(()) if init succeeds, Err(msg) otherwise.
pub fn init_gc0308<I2C: embedded_hal::i2c::I2c>(
    i2c: &mut I2C,
    delay: &mut Delay,
) -> Result<(), &'static str> {
    // Step 1: Full camera power-on sequence (PWDN + RST via AW9523B)
    camera_power_on(i2c, delay);
    delay.delay_millis(20);

    // Step 1b: Generate XCLK on GPIO2 using LEDC timer
    // The GC0308 needs MCLK/XCLK from the ESP32-S3 to run its pixel pipeline.
    // Without XCLK, I2C works but the sensor outputs frozen data.
    // ESP32-S3 LEDC base = 0x6001_4000 (LOW SPEED only, unlike ESP32)
    unsafe {
        // Enable LEDC peripheral clock
        // SYSTEM_PERIP_CLK_EN0_REG: bit 11 = LEDC
        let perip_clk_en0 = 0x600C_0018u32 as *mut u32;
        let clk_en = core::ptr::read_volatile(perip_clk_en0);
        core::ptr::write_volatile(perip_clk_en0, clk_en | (1u32 << 11));
        // Clear reset
        let perip_rst_en0 = 0x600C_0020u32 as *mut u32;
        let rst_en = core::ptr::read_volatile(perip_rst_en0);
        core::ptr::write_volatile(perip_rst_en0, rst_en & !(1u32 << 11));
        
        // ESP32-S3 LEDC register map (low-speed only):
        // Base = 0x6001_4000
        // LEDC_CONF_REG        = base + 0x0D0 = 0x6001_40D0
        //   bit[1:0] = LEDC_APB_CLK_SEL (1=APB 80MHz, 2=RC_FAST, 3=XTAL)
        // LEDC_TIMERx_CONF_REG = base + 0x0A0 + x*8  (timer 0 = 0x0A0)
        //   bits[3:0]  = duty_resolution
        //   bits[21:4] = clock_divider (18 bits)
        //   bit[22]    = pause
        //   bit[23]    = rst
        //   bit[24]    = tick_sel (0=APB, 1=REF_TICK) — NOT USED on S3
        //   bit[25]    = para_up (write 1 to update)
        // LEDC_CHx_CONF0_REG   = base + 0x000 + x*0x14 (ch0 = 0x000)
        //   bits[1:0]  = timer_sel
        //   bit[2]     = sig_out_en
        //   bit[3]     = idle_lv
        //   bit[4]     = para_up
        // LEDC_CHx_HPOINT_REG  = base + 0x004 + x*0x14
        // LEDC_CHx_DUTY_REG    = base + 0x008 + x*0x14
        // LEDC_CHx_CONF1_REG   = base + 0x00C + x*0x14
        //   bit[31]    = duty_start
        
        let ledc_base = 0x6001_4000u32;
        
        // Clock config: use APB (80MHz)
        let ledc_conf = (ledc_base + 0x0D0) as *mut u32;
        core::ptr::write_volatile(ledc_conf, 1u32); // APB_CLK_SEL=1 (80MHz)
        
        // Timer 0: 80MHz, 1-bit resolution, divider=2 → 80/(2*2^1) = 20MHz
        // Or for 10MHz: divider=4 → 80/(4*2^1) = 10MHz
        let timer0_conf = (ledc_base + 0x0A0) as *mut u32;
        // duty_res=1 (bits 3:0), div=2 (bits 21:4), rst=1 (bit 23)
        let timer_val_rst = (1u32) | (2u32 << 4) | (1u32 << 23);
        core::ptr::write_volatile(timer0_conf, timer_val_rst);
        // Clear reset, set para_up
        let timer_val_run = (1u32) | (2u32 << 4) | (1u32 << 25);
        core::ptr::write_volatile(timer0_conf, timer_val_run);
        
        // Channel 0: hpoint=0, duty=1 (50% for 1-bit), timer_sel=0
        let ch0_hpoint = (ledc_base + 0x004) as *mut u32;
        core::ptr::write_volatile(ch0_hpoint, 0u32);
        
        let ch0_duty = (ledc_base + 0x008) as *mut u32;
        core::ptr::write_volatile(ch0_duty, 1u32 << 4); // duty=1 left-shifted by 4
        
        let ch0_conf1 = (ledc_base + 0x00C) as *mut u32;
        core::ptr::write_volatile(ch0_conf1, 1u32 << 31); // duty_start=1
        
        let ch0_conf0 = (ledc_base + 0x000) as *mut u32;
        // timer_sel=0, sig_out_en=1, para_up=1
        core::ptr::write_volatile(ch0_conf0, (1u32 << 2) | (1u32 << 4));
        
        // Route LEDC_LS_SIG_OUT0 (signal 79) to GPIO2
        let func_out_cfg_base = 0x6000_4554u32;
        let gpio2_out_cfg = (func_out_cfg_base + 2 * 4) as *mut u32;
        core::ptr::write_volatile(gpio2_out_cfg, 79u32);
        
        // Enable GPIO2 as output
        let gpio_enable_addr = 0x6000_4020u32 as *mut u32;
        let gpio_en = core::ptr::read_volatile(gpio_enable_addr);
        core::ptr::write_volatile(gpio_enable_addr, gpio_en | (1u32 << 2));
        
        // IO_MUX GPIO2: set to GPIO function (MCU_SEL=1)
        let iomux_gpio2 = (0x6000_9000u32 + 0x04 + 2 * 4) as *mut u32;
        let mux_val = core::ptr::read_volatile(iomux_gpio2);
        core::ptr::write_volatile(iomux_gpio2, (mux_val & !0x7000) | 0x1000);
        
        // Verify: check if GPIO2 toggles
        let in0 = 0x6000_403Cu32 as *const u32;
        let mut tog = 0u32;
        let mut last = (core::ptr::read_volatile(in0) >> 2) & 1;
        for _ in 0..10_000u32 {
            let c = (core::ptr::read_volatile(in0) >> 2) & 1;
            if c != last { tog += 1; last = c; }
        }
        log!("   LEDC XCLK on GPIO2: {} toggles/10k (expect >100)", tog);
    }
    delay.delay_millis(10); // Let XCLK stabilize

    // Step 2: Check chip ID
    if !detect_gc0308(i2c) {
        return Err("GC0308 not detected (chip ID != 0x9B)");
    }

    // Step 3: Write default register table
    for &(reg, val) in GC0308_DEFAULTS.iter() {
        if !sccb_write(i2c, reg, val) {
            return Err("SCCB write failed during defaults");
        }
        // Soft reset needs extra delay
        if reg == 0xfe && val == 0x80 {
            delay.delay_millis(20);
        }
    }
    delay.delay_millis(80); // Allow camera to stabilize

    // QVGA subsample + Y-only output:
    // Subsample gives 320×240 progressive (no interlacing).
    // Y-only (0x24=0x11) gives 1 byte/pixel = 76,800 bytes/frame.
    // This is the proven V96 baseline with halved data rate.
    
    // Enable subsample on Page 1
    sccb_write(i2c, 0xfe, 0x01); // Page 1
    let r53 = sccb_read(i2c, 0x53).unwrap_or(0x00);
    sccb_write(i2c, 0x53, r53 | 0x80); // SUBSAMPLE_EN: bit 7 = 1
    let r55 = sccb_read(i2c, 0x55).unwrap_or(0x00);
    sccb_write(i2c, 0x55, r55 | 0x01); // SUBSAMPLE_EN2: bit 0 = 1
    sccb_write(i2c, 0xfe, 0x00); // Back to Page 0

    // Write subsample config registers (mode, window, etc.)
    for &(reg, val) in GC0308_SUBSAMPLE_QVGA.iter() {
        if !sccb_write(i2c, reg, val) {
            return Err("SCCB write failed during subsample config");
        }
    }

    // Disable crop (subsample handles size reduction)
    sccb_write(i2c, 0x46, 0x00);
    
    delay.delay_millis(100); // Let AEC run a few frames

    // Verify critical registers readback
    let out_en_rb = sccb_read(i2c, REG_OUTPUT_EN).unwrap_or(0xEE);
    let debug_rb = sccb_read(i2c, 0x2e).unwrap_or(0xEE);
    let fmt_rb = sccb_read(i2c, REG_OUTPUT_FORMAT).unwrap_or(0xEE);
    let sync_rb = sccb_read(i2c, 0x26).unwrap_or(0xEE);
    let drv_rb = sccb_read(i2c, 0x1f).unwrap_or(0xEE);
    let mode2_rb = sccb_read(i2c, 0x15).unwrap_or(0xEE);
    let aec_rb = sccb_read(i2c, 0xd0).unwrap_or(0xEE);
    log!("   GC0308 verify: out_en=0x{:02x} debug=0x{:02x} fmt=0x{:02x} sync=0x{:02x} drv=0x{:02x} mode2=0x{:02x} aec=0x{:02x}",
        out_en_rb, debug_rb, fmt_rb, sync_rb, drv_rb, mode2_rb, aec_rb);

    // Verify subsample is disabled
    sccb_write(i2c, 0xfe, 0x01);
    let sub53 = sccb_read(i2c, 0x53).unwrap_or(0xEE);
    let sub54 = sccb_read(i2c, 0x54).unwrap_or(0xEE);
    sccb_write(i2c, 0xfe, 0x00);
    let crop46 = sccb_read(i2c, 0x46).unwrap_or(0xEE);
    log!("   Subsample: P1:0x53=0x{:02x}(expect bit7=0) P1:0x54=0x{:02x}(expect 0x00) crop=0x{:02x}",
        sub53, sub54, crop46);
    log!("   Output format: 0x{:02x} (expect 0x11=Y-only)", fmt_rb);

    Ok(())
}

/// Re-initialize the GC0308 after XCLK is running.
/// Performs soft-reset + full register rewrite.
/// Call this after MCLK/XCLK has been established on GPIO2.
pub fn reinit_gc0308<I2C: embedded_hal::i2c::I2c>(
    i2c: &mut I2C,
    delay: &mut Delay,
) -> Result<(), &'static str> {
    log!("   Re-init GC0308 (QVGA 320x240 Y-only)...");
    
    // Soft reset
    if !sccb_write(i2c, 0xfe, 0x80) {
        return Err("Soft reset write failed");
    }
    delay.delay_millis(30);
    
    // Select page 0
    sccb_write(i2c, 0xfe, 0x00);
    delay.delay_millis(5);
    
    // Verify chip still responds
    if !detect_gc0308(i2c) {
        return Err("GC0308 not detected after soft-reset");
    }
    
    // Write the full Espressif defaults table
    for &(reg, val) in GC0308_DEFAULTS.iter() {
        if !sccb_write(i2c, reg, val) {
            return Err("SCCB write failed during reinit");
        }
        if reg == 0xfe && val == 0x80 {
            delay.delay_millis(20);
        }
    }
    delay.delay_millis(80);
    
    // QVGA subsample + Y-only output (same as init)
    sccb_write(i2c, 0xfe, 0x01); // Page 1
    let r53 = sccb_read(i2c, 0x53).unwrap_or(0x00);
    sccb_write(i2c, 0x53, r53 | 0x80); // SUBSAMPLE_EN: bit 7
    let r55 = sccb_read(i2c, 0x55).unwrap_or(0x00);
    sccb_write(i2c, 0x55, r55 | 0x01); // SUBSAMPLE_EN2: bit 0
    sccb_write(i2c, 0xfe, 0x00);

    for &(reg, val) in GC0308_SUBSAMPLE_QVGA.iter() {
        if !sccb_write(i2c, reg, val) {
            return Err("SCCB write failed during subsample config");
        }
    }

    // Force Y-only output after subsample (defaults may have reset it)
    sccb_write(i2c, 0x24, 0x91); // Y-only, ISP high 8
    sccb_write(i2c, 0x46, 0x00); // Crop off

    delay.delay_millis(100); // Let AEC run a few frames
    
    // Dump key registers to verify
    let dbg = sccb_read(i2c, 0x2e).unwrap_or(0xff);
    let exp_h = sccb_read(i2c, 0x03).unwrap_or(0);
    let exp_l = sccb_read(i2c, 0x04).unwrap_or(0);
    let gain = sccb_read(i2c, 0x50).unwrap_or(0);
    let aaaa = sccb_read(i2c, 0x22).unwrap_or(0xff);
    let r15 = sccb_read(i2c, 0x15).unwrap_or(0xff);
    let r46 = sccb_read(i2c, 0x46).unwrap_or(0xff);
    log!("   Reinit done: dbg=0x{:02x} exp=0x{:02x}{:02x} gain=0x{:02x} aaaa=0x{:02x}", 
        dbg, exp_h, exp_l, gain, aaaa);
    log!("   mode2(0x15)=0x{:02x} crop(0x46)=0x{:02x} (expect 0x0a, 0x00)",
        r15, r46);
    
    // Verify subsample is DISABLED on Page 1
    sccb_write(i2c, 0xfe, 0x01);
    let sub_en = sccb_read(i2c, 0x53).unwrap_or(0xff);
    let sub_en2 = sccb_read(i2c, 0x55).unwrap_or(0xff);
    let sub_mode = sccb_read(i2c, 0x54).unwrap_or(0xff);
    sccb_write(i2c, 0xfe, 0x00);
    let fmt = sccb_read(i2c, 0x24).unwrap_or(0xff);
    log!("   Subsample: en=0x{:02x}(bit7={}) en2=0x{:02x}(bit0={}) mode=0x{:02x} fmt=0x{:02x}",
        sub_en, (sub_en >> 7) & 1, sub_en2, sub_en2 & 1, sub_mode, fmt);
    
    Ok(())
}

