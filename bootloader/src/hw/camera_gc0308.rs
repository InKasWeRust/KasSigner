/// Write a single register via I2C/SCCB
pub fn sccb_write<I2C: embedded_hal::i2c::I2c>(i2c: &mut I2C, reg: u8, val: u8) -> bool {
    i2c.write(GC0308_ADDR, &[reg, val]).is_ok()
}

/// Read a single register via I2C/SCCB
fn sccb_read<I2C: embedded_hal::i2c::I2c>(i2c: &mut I2C, reg: u8) -> Option<u8> {
    let mut buf = [0u8; 1];
    if i2c.write(GC0308_ADDR, &[reg]).is_ok() {
        if i2c.read(GC0308_ADDR, &mut buf).is_ok() {
            return Some(buf[0]);
        }
    }
    None
}

// ═══════════════════════════════════════════════════════════════
// Camera Reset (via AW9523B IO Expander)
// ═══════════════════════════════════════════════════════════════
/// Full camera power-on sequence via AW9523B IO expander
/// Schematic pin map:
///   P0.4 = CAM_PWDN (active HIGH = power down, LOW = active)
///   P1.0 = CAM_RST  (active LOW = reset, HIGH = normal)
///
/// Full camera power-on sequence via AW9523B IO expander
pub fn camera_power_on<I2C: embedded_hal::i2c::I2c>(i2c: &mut I2C, delay: &mut Delay) {
    // Verify AW9523B is responding
    let mut chip_id = [0u8; 1];
    let aw_ok = i2c.write_read(0x58u8, &[0x10u8], &mut chip_id).is_ok();
    log!("   AW9523B chip_id=0x{:02x} (expect 0x23) ok={}", chip_id[0], aw_ok);

    // Read current direction registers to verify they're configured as outputs
    let mut dir_p0 = [0u8; 1];
    let mut dir_p1 = [0u8; 1];
    let _ = i2c.write_read(0x58u8, &[0x04u8], &mut dir_p0);
    let _ = i2c.write_read(0x58u8, &[0x05u8], &mut dir_p1);
    log!("   Direction P0=0x{:02x} P1=0x{:02x} (expect 0x00=all outputs)", dir_p0[0], dir_p1[0]);

    // If directions not set, force them
    if dir_p0[0] != 0x00 {
        log!("   FIXING P0 direction → outputs");
        let _ = i2c.write(0x58u8, &[0x12u8, 0xFFu8]); // LED mode = GPIO
        let _ = i2c.write(0x58u8, &[0x04u8, 0x00u8]); // Direction = output
    }
    if dir_p1[0] != 0x00 {
        log!("   FIXING P1 direction → outputs");
        let _ = i2c.write(0x58u8, &[0x13u8, 0xFFu8]); // LED mode = GPIO
        let _ = i2c.write(0x58u8, &[0x05u8, 0x00u8]); // Direction = output
    }

    // Read current P0 and P1 output states
    let mut p0_val = [0u8; 1];
    let mut p1_val = [0u8; 1];
    let _ = i2c.write_read(0x58u8, &[0x02u8], &mut p0_val);
    let _ = i2c.write_read(0x58u8, &[0x03u8], &mut p1_val);
    let p0 = p0_val[0];
    let p1 = p1_val[0];
    log!("   Before reset: P0=0x{:02x} P1=0x{:02x} (P0.4={} P1.0={})",
        p0, p1, (p0 >> 4) & 1, p1 & 1);

    // Step 1: Assert PWDN HIGH (P0.4 = 1) — camera powered down
    let p0_pwdn_hi = (p0 | 0x10) & !0x00; // Keep other bits, set P0.4
    let _ = i2c.write(0x58u8, &[0x02u8, p0_pwdn_hi]);
    delay.delay_millis(10);

    // Step 2: Assert RST LOW (P1.0 = 0) — camera in reset
    let p1_rst_lo = p1 & !0x01;
    let _ = i2c.write(0x58u8, &[0x03u8, p1_rst_lo]);
    delay.delay_millis(10);

    // Step 3: Deassert PWDN LOW (P0.4 = 0) — camera powered up
    let p0_pwdn_lo = p0_pwdn_hi & !0x10;
    let _ = i2c.write(0x58u8, &[0x02u8, p0_pwdn_lo]);
    delay.delay_millis(20); // Wait for power stabilization

    // Step 4: Deassert RST HIGH (P1.0 = 1) — camera runs
    let p1_rst_hi = p1_rst_lo | 0x01;
    let _ = i2c.write(0x58u8, &[0x03u8, p1_rst_hi]);
    delay.delay_millis(30); // GC0308 needs ~20ms after reset

    // Readback verification
    let _ = i2c.write_read(0x58u8, &[0x02u8], &mut p0_val);
    let _ = i2c.write_read(0x58u8, &[0x03u8], &mut p1_val);
    log!("   After reset: P0=0x{:02x} P1=0x{:02x} (PWDN={} RST={})",
        p0_val[0], p1_val[0], (p0_val[0] >> 4) & 1, p1_val[0] & 1);
}

